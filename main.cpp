// 目标：覆盖实习面试里常见的所有输入输出组织方式与坑位。

// ------------------------------------------------------------
// 01. 固定字段（单组）
// 输入：一行两个整数 a, b
// 输出：a + b
// 要点：最基础 cin/cout

// 02. 固定字段（混合类型）
// 输入：一行，依次为 int x, double y, string z
// 输出：按 "x y(保留2位) z" 输出
// 要点：fixed + setprecision

// 03. 固定多行
// 输入：两行，每行两个整数
// 输出：每行的和，各占一行
// 要点：多行输入映射多行输出

// 04. T 组测试
// 输入：第一行 T，后续 T 行每行两个整数
// 输出：每组输出一行和
// 要点：for/while 循环次数准确

// 05. T 组 + 组内变长
// 输入：第一行 T；每组先给 n，再给 n 个整数
// 输出：每组元素总和
// 要点：嵌套循环

// 06. EOF 连续读取（定长）
// 输入：若干行，每行两个整数，直到 EOF
// 输出：每行和
// 要点：while (cin >> a >> b)

// 07. EOF 连续读取（每行不定长）
// 输入：若干行，每行有不定个整数，直到 EOF
// 输出：每行的和（空行输出 0）
// 要点：getline + stringstream

// 08. 哨兵结束
// 输入：若干行两个整数，遇到 "0 0" 结束
// 输出：终止行之前每行和
// 要点：先判终止再输出

// 09. 记录型输入
// 输入：第一行 n，后续 n 行：name score
// 输出：平均分（保留 2 位）
// 要点：字符串与数字混合读取

// 10. 矩阵输入
// 输入：n m，后接 n 行 m 列整数
// 输出：每行和
// 要点：双层循环

// 11. 多组矩阵（EOF）
// 输入：多组 n m + n*m 数据，直到 EOF
// 输出：每组输出矩阵总和
// 要点：每组状态重置

// 12. 图边列表
// 输入：n m，后接 m 条边 u v（无向图）
// 输出：1..n 每个点度数
// 要点：数组初始化与下标范围

// 13. 命令流解析
// 输入：多行命令（ADD x / MUL x / PRINT / END）
// 输出：每次 PRINT 输出当前值
// 要点：字符串分支解析

// 14. 整行文本统计
// 输入：一整行文本（含空格）
// 输出：单词个数
// 要点：连续空格处理

// 15. 单词反转
// 输入：一整行文本
// 输出：反转单词顺序，单词间仅一个空格
// 要点：标准化空白

// 16. cin + getline 混用
// 输入：先给 n，再给 n 行句子（可含空格）
// 输出：原样逐行输出
// 要点：处理残留换行

// 17. 分隔符解析（CSV 简化）
// 输入：一行 "a,b,c,..."（无引号）
// 输出：字段个数
// 要点：末尾空字段与连续逗号

// 18. 浮点 EOF
// 输入：若干浮点数直到 EOF
// 输出：每个数保留 2 位小数
// 要点：浮点格式一致性

// 19. 异常分支
// 输入：a b（浮点）
// 输出：a/b（保留 6 位）；若 b=0 输出 "Error"
// 要点：分母为零判断

// 20. Case 格式
// 输入：T 组，每组两个整数
// 输出："Case i: ans"
// 要点：编号从 1 开始

// 21. 组间空行控制
// 输入：T 组数据
// 输出：组与组之间空一行，最后一组后不额外空行
// 要点：避免尾部多空行

// 22. 超长整数相加
// 输入：两个非负整数字符串（长度可到 10000）
// 输出：它们的和
// 要点：字符串模拟加法

// 23. 脏输入鲁棒性
// 输入：可能含多个空格、空行、TAB 的文本
// 输出：所有整数的个数
// 要点：空白字符统一处理

// 24. 日志行解析
// 输入：n 行日志，格式 "time|level|message"
// 输出：各 level 出现次数
// 要点：多分隔段提取

// 25. 综合协议输入
// 输入：多组，首字段为类型标记（INT/FLOAT/LINE）
// 输出：按类型执行对应解析并输出
// 要点：状态切换与多模式兼容

